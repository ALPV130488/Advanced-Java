package collection;

import java.util.LinkedHashSet;

//insertion order is sorting

public class LinkedHashSetEx {
	
	public static void main(String[] args) {
		
		LinkedHashSet<String> lhs = new LinkedHashSet<String>();
		
		lhs.add("spring");

		lhs.add("java");

		lhs.add("html");

		lhs.add("css");

		lhs.add("bootstrap");

		System.out.println(lhs);
		
	}

}
